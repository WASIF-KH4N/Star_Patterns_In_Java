class Pattern_12 {
    public static void main(String[] args) {
        for(int i=0;i<=7;i++){
            for(int j=0;j<=7;j++){
                if(i>=1 && j>=1 && i<=6 && j<=6) {
                    System.out.print(" ");
                  }
                    else{
                        System.out.print("*");
                      }
                    }
                  System.out.println();
                }
            }
        }

